public class ComparePrimitives {
  public static void main(String args[]){
    int i = 10;
    short s = 128;
    if(i == s)
      System.out.println("true");
    else
      System.out.println("false");
 
    char c = 10;
    if(i == c)
      System.out.println("true");
    else
      System.out.println("false");
  
    byte b = 127;
    if(i == b)
      System.out.println("true");
    else
      System.out.println("false");
  
  }
}
